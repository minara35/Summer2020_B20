package day18_ForLoop;

public class ForLoop {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("===============");

        // for loop:
        // for( initialization and condition; Iterator){
        // statement; (sout)
        for (int i = 0; i < 10; i++){ // i: 0, 1, 2 ... 10
            System.out.println("Hello World"); // will executed until i < 100;
        }

        for(int n = 0; n < 15; n +=1){
            System.out.println("Minara");
        }
    }
}
